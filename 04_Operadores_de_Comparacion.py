# Operadores de comparacion

# Igualdad - Verifica si dos argumentos a y b son iguales
2 == 2 # True
2 == 3 # False

# Diferente que - Verifica si dos argumentos a y b son dinstintos
2 != 2 # False
2 != 3 # True

# Mayor que - Verifica si el argumento a es mayor que b
2 > 1 # True
2 > 2 # False
2 > 4 # False

# Menor que - Verifica si el argumento a es menor que b
2 < 1 # False
2 < 2 # False
2 < 4 # True

# Mayor o igual - Verifica si el argumento a es mayor o igual que b
2 >= 1 # True
2 >= 2 # True
2 >= 4 # False

# Menor o igual - Verifica si el argumento a es menor o igual que b
2 <= 1 # False
2 <= 2 # True
2 <= 4 # True

